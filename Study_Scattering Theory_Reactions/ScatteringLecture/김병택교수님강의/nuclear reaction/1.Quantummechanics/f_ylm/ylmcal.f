      SUBROUTINE YLCAL(A,LLL,MMM,PKK)
      DIMENSION  PKK(30,30),PK(30,30)
      DOUBLE PRECISION A,EPS,PAI4,SQR4PI,ROOT,PK,Y,FK,TFK,FK1,
     1				 Z,TM1,TM2,TM3,FKP,FAC,T1,T2,H1
      EPS=1.0D-30
      PAI4  =16.0*DATAN(1.0D0) 
      SQR4PI=1.0/DSQRT(PAI4)
      ROOT=A
      LP1MAX=LLL
      MP1MAX=MMM
      PK(1,1)=SQR4PI
      PK(1,2)=0.0
      DO 620 M=3,5
      PK(1,M)=0.0
  620 PK(2,M)=0.0
      PK(2,1)=ROOT*SQR4PI
      Y=1.0-ROOT*ROOT
      IF(Y.LT.0.0) Y=0.0
      Y= DSQRT(Y)
      PK(2,2)=Y*SQR4PI
      IF(LP1MAX.LE.2) GO TO 660
      FK=1.0
      TFK=1.0
      DO 650 K=3,LP1MAX
      FK=FK+1.0
      FK1=FK-1.0
      TFK=TFK+2.0
      PK(K,1)=(TFK*ROOT*PK(K-1,1)-FK1*PK(K-2,1))/FK
      PK(K,2)=(TFK*ROOT*PK(K-1,2)-FK *PK(K-2,2))/FK1
CCCCC
      IF(DABS(FK).LT.EPS.OR.DABS(FK1).LT.EPS) 
     1WRITE(6,998) ROOT,LP1MAX,MP1MAX
  998 FORMAT(1H   ,'CHECK FOR ROOT, LP1MAX, MP1MAX=',E10.2,2I5)
CCCCC
	if (y.lt.1.0d-10) go to 640
      Z=ROOT/Y
      TM1=0.0
      TM2=FK+1.0
      TM3=FK
      DO 635 MP=3,MP1MAX
      IF(MP.GT.K) GO TO 630
      TM1=TM1+2.0
      TM2=TM2-1.0
      TM3=TM3+1.0
      PK(K,MP)=TM1*Z*PK(K,MP-1)-TM2*TM3*PK(K,MP-2)
      GO TO 635
  630 PK(K,MP)=0.0
  635 CONTINUE
      GO TO 650
  640 DO 645 MP=3,MP1MAX
  645 PK(K,MP)=0.
  650 CONTINUE
  660 IF (LP1MAX.EQ.1) GO TO 1000 !RETURN
      DO 420 LP1=2,LP1MAX
      FK=LP1-1
      FKP=FK+1.0
      FAC=1.0
      H1=DSQRT(FK*2.0+1.0) 
      PK(LP1,1)=PK(LP1,1)*H1
      MP1X=MIN0(LP1,MP1MAX)
      DO 410 MP1=2,MP1X
      T1=-FAC/DSQRT(FK*FKP) 
      FAC=T1
      IF(DABS(FK).LT.EPS.OR.DABS(FKP).LT.EPS)
     1WRITE(6,999) ROOT,LP1,MP1
  999 FORMAT(1H  ,'CHECK FOR ROOT, LP1,MP1=',E10.2,2I5)
      T2=PK(LP1,MP1)*FAC*H1
      PK(LP1,MP1)=T2
      FK=FK-1.0
      FKP=FKP+1.0
  410 CONTINUE
  420 CONTINUE
 1000 DO 800 LP1=1,LP1MAX
      DO 800 MP1=1,MP1MAX
	PKK(LP1,MP1)=PK(LP1,MP1)
  800 CONTINUE 
      RETURN
      END
                                                                        
